class WideningInt
{
 public static void main(String[]args)
 {
  {
   int a=71;
   int d=a;
   System.out.println(a);
   System.out.println(d);
  }
  {
   int a=72;
   char d=a;
   System.out.println(a);
   System.out.println(d);
  }
  {
   int a=73;
   long d=a;
   System.out.println(a);
   System.out.println(d);
  }
  {
   int a=74;
   float d=a;
   System.out.println(a);
   System.out.println(d);
  }
  {
   int a=75;
   double d=a;
   System.out.println(a);
   System.out.println(d);
  }
 }
}