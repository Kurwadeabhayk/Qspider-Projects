class LargeSmall
{ 
 public static void main(String[]args)
 {
  {
  double a=20;
  double b=20.110;
  String ans= a > b ? a+ ": is larger" : b+ ": is larger"; 
  System.out.println(ans);
  }
  {
  double a=20;
  double b=20.110;
  String ans= a < b ? a+ ": is smaller" : b+ ": is smaller"; 
  System.out.println(ans);
  }
 }
}
